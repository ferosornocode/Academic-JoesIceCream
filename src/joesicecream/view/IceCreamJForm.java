    private void calcularBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularBotonActionPerformed
        // TODO add your handling code here:
        if(chocolateRadio.isSelected())
            subtotal = subtotal + 10;
        else if(vainillaRadio.isSelected())
            subtotal = subtotal + 15;
        else if(fresaRadio.isSelected())
            subtotal = subtotal + 12;
        
        if(chocolateCheck.getState())
            subtotal = subtotal + 3;
        
        if(chispasChocCheck.getState())
            subtotal = subtotal + 5;
        
        if(chispasSaboresCheck.getState())
            subtotal = subtotal + 3;
        
        if(jaleaCheck.getState())
            subtotal = subtotal + 7;
        
        subtotalText.setText(subtotal+"");
        
        ivaCalculado = subtotal*IVA;
        ivaText.setText(ivaCalculado+"");
        
        total = subtotal + ivaCalculado;
        totalText.setText(total+"");
        
        
    }//GEN-LAST:event_calcularBotonActionPerformed

